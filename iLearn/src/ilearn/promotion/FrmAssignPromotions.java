    @Action
    public void save()
    {
        for (int i = 0; i < tblStudents.getRowCount(); i++)
        {
            boolean promoted = (Boolean) tblStudents.getValueAt(i, 2);
            if (promoted)
            {
                String id = tblStudents.getValueAt(i, 0).toString();
                String newClass = tblStudents.getValueAt(i, 3).toString();
                Promote.addPromotion(id, newClass);
            }
        }
        boolean savePromotions = Promote.savePromotionDetails();
        if (savePromotions)
        {
            String message = "The students were successfully promoted to their new class. \n"
                             + "Would you like to continue?";
            int response = Utilities.showConfirmDialog(rootPane, message);
            if (response == JOptionPane.YES_OPTION)
            {
                resetForm();
            }
            else
            {
                this.dispose();
            }
        }
    }

    private void populateLists()
    {
        ArrayList<String> clsList = Classes.getClassList();
        cmbClass.setModel(new DefaultComboBoxModel(clsList.toArray()));
    }

    @Action
    public void loadClass()
    {
        String clsCode = cmbClass.getSelectedItem().toString();
        tblStudents.setModel(Promote.getStudentList(clsCode));
        JComboBox comboBox = new JComboBox(Classes.getClassList().toArray());
        comboBox.setEditable(true);
        DefaultCellEditor editor = new DefaultCellEditor(comboBox);
        // Assign the editor to the fourth column
        TableColumnModel tcm = tblStudents.getColumnModel();
        tcm.getColumn(3).setCellEditor(editor);
        Promote.resetPromotions();
    }

    private void resetForm()
    {
        remove(wrapperPanel);
        initComponents();
        populateLists();
    }
