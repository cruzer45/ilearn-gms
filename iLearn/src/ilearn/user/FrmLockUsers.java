    @Action
    public Task lockUsers()
    {
        String message = "Are you sure you want to lock non-administrator users?";
        int response = Utilities.showConfirmDialog(rootPane, message);
        if (response == JOptionPane.YES_OPTION)
        {
            return new LockUsersTask(org.jdesktop.application.Application.getInstance());
        }
        else
        {
            return null;
        }

    }

    private class LockUsersTask extends org.jdesktop.application.Task<Object, Void>
    {

        LockUsersTask(org.jdesktop.application.Application app)
        {
            super(app);
        }

        @Override
        protected Object doInBackground()
        {
            boolean lockUsers = User.lockUsers();
            return lockUsers;  // return your result
        }

        @Override
        protected void succeeded(Object result)
        {
            if (result.equals(Boolean.TRUE))
            {
                String message = "Users were successfully locked.";
                Utilities.showInfoMessage(rootPane, message);
            }
            else
            {
                String message = "An error occurred while trying to lock the users.";
                Utilities.showWarningMessage(rootPane, message);
            }
        }
    }
