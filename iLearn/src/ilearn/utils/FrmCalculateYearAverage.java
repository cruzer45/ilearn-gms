    @Action(block = Task.BlockingScope.COMPONENT)
    public Task calculate()
    {
        return new CalculateTask(org.jdesktop.application.Application.getInstance());
    }

    private class CalculateTask extends org.jdesktop.application.Task<Object, Void>
    {

        String sem1;
        String sem2;

        CalculateTask(org.jdesktop.application.Application app)
        {
            super(app);
            sem1 = Term.getTermIDFromShortName(cmbFirst.getSelectedItem().toString());
            sem2 = Term.getTermIDFromShortName(cmbSecond.getSelectedItem().toString());
            if (sem1.equals("---Select One---") || sem2.equals("---Select One---"))
            {
                String message = "Kindly select the semester to process.";
                Utilities.showWarningMessage(rootPane, message);
                return;
            }
        }

        @Override
        protected Object doInBackground()
        {
            Grade.calculateYearAverage(sem1, sem2);
            return null;  // return your result
        }
    }
