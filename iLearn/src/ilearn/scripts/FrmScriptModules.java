    private void tblScriptsMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_tblScriptsMouseClicked
    {
        //GEN-HEADEREND:event_tblScriptsMouseClicked
        if (evt.getClickCount() >= 2 && tblScripts.getSelectedRow() != -1)
        {
            FrmScriptModuleDetails frmScriptModuleDetails = new FrmScriptModuleDetails(null, true);
            frmScriptModuleDetails.setLocationRelativeTo(this);
            frmScriptModuleDetails.setVisible(true);
            reload();
        }
    }//GEN-LAST:event_tblScriptsMouseClicked

    @Action
    public void cancel()
    {
        Utilities.showCancelScreen(this);
    }

    @Action
    public void remove()
    {
        if (tblScripts.getSelectedRow() != -1)
        {
            String message = "Are you sure you want to remove the selected item(s)?";
            int response = Utilities.showConfirmDialog(rootPane, message);
            if (response == JOptionPane.YES_OPTION)
            {
                for (int row : tblScripts.getSelectedRows())
                {
                    int selectedModule = Integer.valueOf(tblScripts.getValueAt(row, 0).toString());
                    ScriptModules.deleteScriptModule(selectedModule);
                }
                reload();
            }
        }
        else
        {
            String message = "You must select an item to remove.";
            Utilities.showWarningMessage(rootPane, message);
        }
    }

    @Action
    public void add()
    {
        FrmScriptModuleDetails frmScriptModuleDetails = new FrmScriptModuleDetails(null, true);
        frmScriptModuleDetails.setLocationRelativeTo(this);
        frmScriptModuleDetails.setVisible(true);
        reload();
    }

    @Action
    public void reload()
    {
        tblScripts.setModel(ScriptModules.getScriptModuleTable());
        TableColumnAdjuster tca = new TableColumnAdjuster(tblScripts);
        tca.adjustColumns();
    }
